// ── Script Generation ────────────────────────────────────────

function esc(s) {
  return String(s ?? '').replace(/[$`"\\]/g, '\\$&')
}

function generateScript(cfg) {
  const L = []
  const add = (...lines) => L.push(...lines)
  const sec = (t) => add('', `# ── ${t} ${'─'.repeat(Math.max(0, 54 - t.length))}`)

  add('#!/bin/bash')
  add('set -e')
  add('export DEBIAN_FRONTEND=noninteractive')
  add('')
  add('# ============================================================')
  add('# Ubuntu Bootstrap Script')
  add('# Generated by AdminToolbox Bootstrap Generator')
  add(`# Generated: ${new Date().toUTCString()}`)
  add('# ============================================================')
  add('')

  if (cfg.logging) {
    sec('Logging')
    add('mkdir -p /var/log')
    add('exec > >(tee -a /var/log/bootstrap.log) 2>&1')
    add('echo "[bootstrap] Script started: $(date)"')
  }

  sec('Preflight checks')
  add('if [ "$(id -u)" -ne 0 ]; then')
  add('  echo "ERROR: This script must be run as root." >&2')
  add('  exit 1')
  add('fi')
  add('echo "[bootstrap] OS: $(. /etc/os-release && echo \\"$PRETTY_NAME\\")"')

  sec('System update')
  add('echo "[bootstrap] Updating system packages..."')
  add('apt-get update -qq')
  add('apt-get upgrade -y -qq')

  if (cfg.hostname) {
    sec('Hostname')
    add(`echo "[bootstrap] Setting hostname: ${esc(cfg.hostname)}"`)
    add(`hostnamectl set-hostname "${esc(cfg.hostname)}"`)
    add(`echo "${esc(cfg.hostname)}" > /etc/hostname`)
    add(`# Ensure /etc/hosts entry`)
    add(`if ! grep -qw "${esc(cfg.hostname)}" /etc/hosts; then`)
    add(`  sed -i "1a\\\\127.0.1.1 ${esc(cfg.hostname)}" /etc/hosts`)
    add('fi')
  }

  if (cfg.timezone && cfg.timezone !== 'UTC') {
    sec('Timezone')
    add(`echo "[bootstrap] Setting timezone: ${esc(cfg.timezone)}"`)
    add(`timedatectl set-timezone "${esc(cfg.timezone)}"`)
  }

  if (cfg.locale && cfg.locale !== 'en_US.UTF-8') {
    sec('Locale')
    add(`echo "[bootstrap] Setting locale: ${esc(cfg.locale)}"`)
    add('apt-get install -y -qq locales')
    add(`locale-gen "${esc(cfg.locale)}"`)
    add(`update-locale LANG="${esc(cfg.locale)}"`)
  }

  if (cfg.keyboard && cfg.keyboard !== 'us') {
    sec('Keyboard layout')
    add(`echo "[bootstrap] Setting keyboard: ${esc(cfg.keyboard)}"`)
    add(`cat > /etc/default/keyboard << 'KBEOF'`)
    add(`XKBMODEL="pc105"`)
    add(`XKBLAYOUT="${esc(cfg.keyboard)}"`)
    add(`XKBVARIANT=""`)
    add(`XKBOPTIONS=""`)
    add('KBEOF')
    add(`dpkg-reconfigure -f noninteractive keyboard-configuration 2>/dev/null || true`)
  }

  if (cfg.ntp) {
    sec('NTP / Time synchronization')
    add('echo "[bootstrap] Enabling NTP..."')
    add('timedatectl set-ntp true')
    if (cfg.ntpServer) {
      add(`echo "[bootstrap] Setting custom NTP server: ${esc(cfg.ntpServer)}"`)
      add('apt-get install -y -qq chrony')
      add(`sed -i '/^pool /d; /^server /d' /etc/chrony/chrony.conf 2>/dev/null || true`)
      add(`echo "server ${esc(cfg.ntpServer)} iburst" >> /etc/chrony/chrony.conf`)
      add('systemctl enable --now chrony')
    }
  }

  const pkgs = cfg.packages.filter(Boolean)
  if (pkgs.length > 0) {
    sec('Package installation')
    add(`echo "[bootstrap] Installing packages..."`)
    add(`apt-get install -y -qq \\`)
    pkgs.forEach((p, i) => add(`  ${p}${i < pkgs.length - 1 ? ' \\' : ''}`))
  }

  if (cfg.dns) {
    const servers = cfg.dns.trim().split(/[\s,]+/).filter(Boolean)
    if (servers.length > 0) {
      sec('DNS configuration')
      add('echo "[bootstrap] Setting DNS servers..."')
      add('mkdir -p /etc/systemd/resolved.conf.d')
      add(`cat > /etc/systemd/resolved.conf.d/custom-dns.conf << 'DNSEOF'`)
      add('[Resolve]')
      add(`DNS=${servers.join(' ')}`)
      add('DNSEOF')
      add('systemctl restart systemd-resolved 2>/dev/null || true')
    }
  }

  if (cfg.ufw) {
    sec('Firewall (UFW)')
    add('echo "[bootstrap] Configuring UFW..."')
    add('apt-get install -y -qq ufw')
    add('ufw --force reset')
    add('ufw default deny incoming')
    add('ufw default allow outgoing')
    const sshP = Number(cfg.sshPort) || 22
    add(`ufw allow ${sshP}/tcp   # SSH — always allowed`)
    for (const p of cfg.ufwPorts) {
      if (String(p) !== String(sshP)) add(`ufw allow ${p}/tcp`)
    }
    add('ufw --force enable')
    add('ufw status verbose')
  }

  sec('SSH configuration')
  add('echo "[bootstrap] Configuring SSH..."')
  add('CFG=/etc/ssh/sshd_config')
  const sshPort = Number(cfg.sshPort) || 22
  add(`sed -i "s/^#*\\s*Port .*/Port ${sshPort}/" "$CFG"`)
  add(`grep -qE "^Port " "$CFG" || echo "Port ${sshPort}" >> "$CFG"`)
  const rootVal = cfg.permitRootLogin ? 'yes' : 'no'
  add(`sed -i "s/^#*\\s*PermitRootLogin .*/PermitRootLogin ${rootVal}/" "$CFG"`)
  add(`grep -qE "^PermitRootLogin " "$CFG" || echo "PermitRootLogin ${rootVal}" >> "$CFG"`)
  const pwVal = cfg.passwordAuth ? 'yes' : 'no'
  add(`sed -i "s/^#*\\s*PasswordAuthentication .*/PasswordAuthentication ${pwVal}/" "$CFG"`)
  add(`grep -qE "^PasswordAuthentication " "$CFG" || echo "PasswordAuthentication ${pwVal}" >> "$CFG"`)
  add('systemctl restart sshd 2>/dev/null || systemctl restart ssh 2>/dev/null || true')

  const hasCleanup = cfg.removeSnap || cfg.disableCloudInit || cfg.removeServices || cfg.aptClean
  if (hasCleanup) {
    sec('Cleanup / Debloat')

    if (cfg.removeSnap) {
      add('echo "[bootstrap] Removing snapd..."')
      add('systemctl stop snapd.service snapd.socket 2>/dev/null || true')
      add('apt-get purge -y snapd 2>/dev/null || true')
      add('apt-mark hold snapd 2>/dev/null || true')
      add('rm -rf /snap /var/snap /var/lib/snapd /var/cache/snapd /usr/lib/snapd 2>/dev/null || true')
    }

    if (cfg.disableCloudInit) {
      add('echo "[bootstrap] Disabling cloud-init..."')
      add('touch /etc/cloud/cloud-init.disabled')
      add('for svc in cloud-init cloud-init-local cloud-config cloud-final; do')
      add('  systemctl disable "$svc" 2>/dev/null || true')
      add('  systemctl stop    "$svc" 2>/dev/null || true')
      add('done')
    }

    if (cfg.removeServices) {
      add('echo "[bootstrap] Removing unnecessary services..."')
      add('for pkg in whoopsie apport popularity-contest ubuntu-report; do')
      add('  dpkg -s "$pkg" &>/dev/null 2>&1 && apt-get purge -y "$pkg" || true')
      add('done')
    }

    if (cfg.aptClean) {
      add('echo "[bootstrap] Running apt cleanup..."')
      add('apt-get autoremove -y -qq')
      add('apt-get clean')
    }
  }

  sec('Done')
  add('echo ""')
  add('echo "════════════════════════════════════════════════════"')
  add('echo " Bootstrap completed successfully: $(date)"')
  if (cfg.logging) add('echo " Full log: /var/log/bootstrap.log"')
  add('echo "════════════════════════════════════════════════════"')

  return L.join('\n') + '\n'
}

