function buildContextWarnings(cfg) {
  const warns = []

  if (cfg.permitRootLogin) {
    warns.push({ level: 'warn', text: 'Root-Login per SSH ist aktiviert. Dies ist unsicher und sollte nur in kontrollierten Umgebungen verwendet werden.' })
  }
  if (cfg.passwordAuth) {
    warns.push({ level: 'warn', text: 'Passwort-Authentifizierung ist aktiviert. Für höhere Sicherheit empfiehlt sich ausschließlich Key-basierte Authentifizierung.' })
  }
  if (cfg.sshPort && Number(cfg.sshPort) !== 22) {
    warns.push({ level: 'info', text: `SSH-Port ist auf ${cfg.sshPort} geändert. Stelle sicher, dass dieser Port in der Firewall erlaubt ist — sonst verlierst du den Zugang.` })
  }
  if (cfg.ufw && cfg.ufwPorts.length === 0) {
    warns.push({ level: 'warn', text: 'Firewall aktiviert, aber keine zusätzlichen Ports definiert. Nur der SSH-Port ist erreichbar.' })
  }
  if (cfg.removeSnap && cfg.packages.includes('snapd')) {
    warns.push({ level: 'info', text: 'snapd ist gleichzeitig als Paket ausgewählt und soll entfernt werden. Das Entfernen überschreibt die Installation.' })
  }

  if (!warns.length) return ''

  return `<div class="bg-ctx-warns">${warns.map(w =>
    `<div class="bg-ctx-warn bg-ctx-${w.level}">
      <span class="bg-ctx-icon">${w.level === 'warn' ? '⚠' : 'ℹ'}</span>
      <span>${w.text}</span>
    </div>`
  ).join('')}</div>`
}

