function badge(val, trueLabel = 'Aktiviert', falseLabel = 'Deaktiviert') {
  const ok = Boolean(val)
  return `<span class="bg-badge ${ok ? 'bg-badge-ok' : 'bg-badge-off'}">${ok ? trueLabel : falseLabel}</span>`
}

function reviewRow(label, value) {
  return `<div class="bg-rev-row"><span class="bg-rev-label">${label}</span><span class="bg-rev-val">${value}</span></div>`
}

