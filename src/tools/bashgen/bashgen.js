// ── Init ──────────────────────────────────────────────────────

export function init(container) {
  // State
  const ports = []
  let generatedScript = ''

  // Elements
  const s1 = container.querySelector('#bg-s1')
  const s2 = container.querySelector('#bg-s2')
  const s3 = container.querySelector('#bg-s3')

  function showStep(n) {
    s1.hidden = n !== 1
    s2.hidden = n !== 2
    s3.hidden = n !== 3
    for (let i = 1; i <= 3; i++) {
      const el = container.querySelector(`#bg-prog-${i}`)
      el.classList.toggle('bg-prog-active', i === n)
      el.classList.toggle('bg-prog-done', i < n)
    }
  }

  // UFW toggle
  const ufwCb    = container.querySelector('#bg-ufw')
  const ufwOpts  = container.querySelector('#bg-ufw-opts')
  ufwCb.addEventListener('change', () => { ufwOpts.hidden = !ufwCb.checked })

  // Port management
  const portInput  = container.querySelector('#bg-port-input')
  const portAddBtn = container.querySelector('#bg-port-add-btn')
  const portTags   = container.querySelector('#bg-port-tags')
  const portErr    = container.querySelector('#bg-port-err')

  function renderPorts() {
    portTags.innerHTML = ports.map((p, i) => `
      <span class="bg-port-tag">
        ${p}
        <button class="bg-port-tag-rm" data-i="${i}" title="Entfernen">×</button>
      </span>`).join('')
    portTags.querySelectorAll('.bg-port-tag-rm').forEach(btn => {
      btn.addEventListener('click', () => { ports.splice(parseInt(btn.dataset.i), 1); renderPorts() })
    })
  }

  function addPorts() {
    portErr.textContent = ''
    const vals = portInput.value.trim().split(/[\s,]+/).filter(Boolean)
    if (!vals.length) return
    for (const v of vals) {
      const n = parseInt(v, 10)
      if (isNaN(n) || n < 1 || n > 65535) { portErr.textContent = `Ungültiger Port: "${v}"`; return }
      if (!ports.includes(n)) ports.push(n)
    }
    portInput.value = ''
    renderPorts()
  }

  portAddBtn.addEventListener('click', addPorts)
  portInput.addEventListener('keydown', e => { if (e.key === 'Enter') { e.preventDefault(); addPorts() } })

  // Read config from form
  function readConfig() {
    const sshPort = parseInt(container.querySelector('#bg-ssh-port').value) || 22
    return {
      hostname:       container.querySelector('#bg-hostname').value.trim(),
      timezone:       container.querySelector('#bg-timezone').value,
      locale:         container.querySelector('#bg-locale').value,
      keyboard:       container.querySelector('#bg-keyboard').value,
      logging:        container.querySelector('#bg-logging').checked,
      sshPort,
      permitRootLogin: container.querySelector('#bg-root-login').checked,
      passwordAuth:   container.querySelector('#bg-pw-auth').checked,
      ntp:            container.querySelector('#bg-ntp').checked,
      ntpServer:      container.querySelector('#bg-ntp-server').value.trim(),
      packages:       [...container.querySelectorAll('.bg-pkg-cb:checked')].map(el => el.value),
      removeSnap:     container.querySelector('#bg-rm-snap').checked,
      disableCloudInit: container.querySelector('#bg-rm-cloud').checked,
      removeServices: container.querySelector('#bg-rm-svc').checked,
      aptClean:       container.querySelector('#bg-apt-clean').checked,
      ufw:            container.querySelector('#bg-ufw').checked,
      ufwPorts:       [...ports],
      dns:            container.querySelector('#bg-dns').value.trim(),
    }
  }

  // Step 1 → Step 2
  container.querySelector('#bg-to-review').addEventListener('click', () => {
    const cfg = readConfig()

    container.querySelector('#bg-review-body').innerHTML    = buildReview(cfg)
    container.querySelector('#bg-ctx-warns').innerHTML      = buildContextWarnings(cfg)

    // Reset confirmations
    container.querySelector('#bg-confirm-1').checked = false
    container.querySelector('#bg-confirm-2').checked = false
    container.querySelector('#bg-generate').disabled  = true

    showStep(2)
    container.querySelector('#bg-s2').scrollIntoView({ behavior: 'smooth', block: 'start' })
  })

  // Confirm checkboxes enable/disable generate button
  const confirm1 = container.querySelector('#bg-confirm-1')
  const confirm2 = container.querySelector('#bg-confirm-2')
  const generateBtn = container.querySelector('#bg-generate')

  function updateGenerateBtn() {
    generateBtn.disabled = !(confirm1.checked && confirm2.checked)
  }
  confirm1.addEventListener('change', updateGenerateBtn)
  confirm2.addEventListener('change', updateGenerateBtn)

  // Back to step 1
  container.querySelector('#bg-back').addEventListener('click', () => showStep(1))

  // Step 2 → Step 3: generate script
  generateBtn.addEventListener('click', async () => {
    const cfg    = readConfig()
    generatedScript = generateScript(cfg)

    showStep(3)

    // Request temp link from backend
    const tlLoading = container.querySelector('#bg-tl-loading')
    const tlResult  = container.querySelector('#bg-tl-result')
    const tlErr     = container.querySelector('#bg-tl-err')
    tlLoading.hidden = false
    tlResult.hidden  = true
    tlErr.textContent = ''

    try {
      const res  = await fetch('/api/scriptstore', {
        method:  'POST',
        headers: { 'Content-Type': 'application/json' },
        body:    JSON.stringify({ script: generatedScript })
      })
      const data = await res.json()

      if (!res.ok) { tlErr.textContent = data.error || 'Fehler beim Erstellen des Links.'; tlLoading.hidden = true; return }

      const url     = `${window.location.origin}/script/${data.token}.sh`
      const curlCmd = `bash <(curl -fsSL "${url}")`
      const expires = new Date(data.expires)

      container.querySelector('#bg-tl-url').value      = url
      container.querySelector('#bg-tl-cmd').textContent = curlCmd
      container.querySelector('#bg-tl-expires').textContent =
        `Link läuft ab um: ${expires.toLocaleTimeString('de-DE')} (in 5 Minuten)`

      tlLoading.hidden = true
      tlResult.hidden  = false
    } catch {
      tlErr.textContent = 'Backend nicht erreichbar. Direkter Download weiterhin verfügbar.'
      tlLoading.hidden = true
    }

    container.querySelector('#bg-s3').scrollIntoView({ behavior: 'smooth', block: 'start' })
  })

  // Direct download
  container.querySelector('#bg-dl-btn').addEventListener('click', () => {
    const blob = new Blob([generatedScript], { type: 'text/plain;charset=utf-8' })
    const url  = URL.createObjectURL(blob)
    const a    = Object.assign(document.createElement('a'), { href: url, download: 'bootstrap.sh' })
    a.click()
    URL.revokeObjectURL(url)
  })

  // Copy buttons
  function makeCopyBtn(btnId, getValue) {
    container.querySelector(btnId)?.addEventListener('click', function () {
      navigator.clipboard.writeText(getValue()).then(() => {
        const orig = this.textContent
        this.textContent = 'Kopiert!'
        this.disabled = true
        setTimeout(() => { this.textContent = orig; this.disabled = false }, 1500)
      })
    })
  }

  makeCopyBtn('#bg-tl-copy-url', () => container.querySelector('#bg-tl-url').value)
  makeCopyBtn('#bg-tl-copy-cmd', () => container.querySelector('#bg-tl-cmd').textContent)

  // Restart
  container.querySelector('#bg-restart').addEventListener('click', () => {
    generatedScript = ''
    showStep(1)
  })
}
